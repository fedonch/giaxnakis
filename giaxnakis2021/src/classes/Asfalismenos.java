package classes;

public class Asfalismenos {

	private int amka;
	private String name;
	private String city;
	
	public Asfalismenos(int amka, String name,String city) {
		this.amka=amka;
		this.name=name;
		this.city=city;
		
	}
	
	public int getAmka() {
		return amka;
	}
	
	public String getName() {
		return name;
	}
	
	public String getCity() {
		return city;
	}
	
}
